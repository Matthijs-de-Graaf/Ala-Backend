	<h1 class="black">Documentbeheersysteem</h1>
<p>Welkom bij ons online documentbeheersysteem!</p>
<p>Wij zijn een team van vier studenten van MBO Rijnland, en wij hebben in opdracht van het Ministerie van Binnenlandse Zaken een systeem ontwikkeld om ambtenaren te helpen bij het efficiënt beheren van documenten.</p>
<p>Ons systeem is ontworpen om het gemakkelijker te maken voor ambtenaren om te beslissen welke documenten moeten worden bewaard en welke kunnen worden verwijderd. Hiermee krijg je een efficiëntere werkstroom.</p>
<p>Gebruik onze <a href="Retrieve.php">vragenlijst</a> om snel en gemakkelijk uw documenten te ordenen. De vragenlijst zal u helpen bij het identificeren van documenten die moeten worden bewaard en documenten die kunnen worden verwijderd.</p>
<p>Onze <a href="DocumentenCheck.php">DocuCheck</a>-pagina biedt u de mogelijkheid om snel en gemakkelijk bestanden te selecteren die u wilt laten beoordelen om te beslissen of ze bewaard moeten blijven. Daarnaast hebben we ook een handige functie ingebouwd waarmee u een speciale map kunt aanmaken waarin de beoordeelde bestanden worden geplaatst. Deze map is ingedeeld in twee secties: één voor bestanden die moeten worden bewaard en één voor bestanden die kunnen worden verwijderd.</p>
<p>Door deze functie te gebruiken, kunt u gemakkelijk en efficiënt uw documenten beheren. U kunt snel toegang krijgen tot de bestanden die u wilt bewaren en deze vervolgens in de juiste map plaatsen. Dit helpt niet alleen bij het creëren van een georganiseerde workflow, maar ook bij het waarborgen van de privacy van gevoelige informatie.</p>
<p>We zijn er trots op dat ons systeem u helpt bij het beheren van uw documenten op een efficiënte en effectieve manier. Als u nog vragen of opmerkingen heeft, neem dan gerust contact met ons op.</p></p>
